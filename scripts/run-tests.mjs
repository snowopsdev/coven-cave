/** Build the `node` argv (flags + file) for a single test path. */
export function nodeArgsFor(file) {
  const args = [];
  if (file.endsWith(".ts") || STRIP_TYPES_MJS.has(file)) {
    args.push("--experimental-strip-types");
  }
  if (ALIAS_LOADER.has(file)) {
    args.push("--import", "./scripts/test-alias-register.mjs");
  }
  args.push(file);
  return args;
}

function main(argv) {
  const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
  let names = argv.length ? argv : ["all"];
  if (names.includes("all")) names = Object.keys(SUITES);
  const unknown = names.filter((n) => !SUITES[n]);
  if (unknown.length) {
    console.error(`unknown suite(s): ${unknown.join(", ")}. known: ${[...Object.keys(SUITES), "all"].join(", ")}`);
    process.exit(2);
  }
  const list = names.flatMap((n) => SUITES[n]);
  console.log(`running ${list.length} test file(s) [${names.join(", ")}]`);
  let passed = 0;
  for (const file of list) {
    const res = spawnSync(process.execPath, nodeArgsFor(file), { stdio: "inherit", cwd: root });
    if (res.status !== 0) {
      console.error(`\n✗ FAILED: ${file}  (${passed} passed before it)`);
      process.exit(res.status ?? 1);
    }
    passed++;
  }
  console.log(`\n✓ ${passed} test file(s) passed [${names.join(", ")}]`);
}

// Run only when invoked directly, so check-tests-wired.mjs can import SUITES.
if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main(process.argv.slice(2));
}
